@Composable
fun AvatarEmoji(
    name: String,
    colors: List<Color>,
    size: Dp = AvatarUtils.DEFAULT_SIZE,
    shape: Shape = AvatarUtils.DEFAULT_SHAPE,
    modifier: Modifier = Modifier
) {
    val context = remember(name, colors) { AvatarUtils.createContext(name, colors) }
    
    val bgColor = context.getRandomColor()
    val emojiIndex = abs(context.numFromName) % EMOJI_POOL.size
    val emoji = EMOJI_POOL[emojiIndex]

    Box(
        modifier = modifier
            .size(size)
            .clip(shape)
            .background(bgColor),
        contentAlignment = Alignment.Center
    ) {
        val fontSize = with(LocalDensity.current) { (size * 0.65f).toSp() }
        Text(
            text = emoji,
            fontSize = fontSize
        )
    }
}

