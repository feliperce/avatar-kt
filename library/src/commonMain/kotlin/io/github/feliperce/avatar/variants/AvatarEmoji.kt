internal data class EmojiData(
    val backgroundColor: Color,
    val emoji: String
)

internal fun generateEmojiData(name: String, colors: List<Color>): EmojiData {
    val numFromName = AvatarUtils.hashCode(name)
    val bgColor = AvatarUtils.getRandomColor(numFromName, colors, colors.size)
    val emojiIndex = AvatarUtils.getUnit(numFromName, EMOJI_POOL.size)

    return EmojiData(
        backgroundColor = bgColor,
        emoji = EMOJI_POOL[emojiIndex]
    )
}

/**
 * Renders the Emoji variant of BoringAvatar.
 * It selects a random emoji from a large predefined pool and renders it over a solid random background.
 *
 * @param name The generated hash base string.
 * @param colors The color palette to pick from.
 * @param size The size of the avatar.
 * @param shape The clipping shape for the canvas.
 * @param modifier Additional compose modifiers.
 */
@Composable
fun AvatarEmoji(
    name: String,
    colors: List<Color>,
    size: Dp = 40.dp,
    shape: Shape = CircleShape,
    modifier: Modifier = Modifier
) {
    val data = remember(name, colors) { generateEmojiData(name, colors) }

    Box(
        modifier = modifier
            .size(size)
            .clip(shape)
            .background(data.backgroundColor),
        contentAlignment = Alignment.Center
    ) {
        val fontSize = with(LocalDensity.current) { (size * 0.65f).toSp() }
        Text(
            text = data.emoji,
            fontSize = fontSize
        )
    }
}

