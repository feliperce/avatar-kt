internal data class EmojiData(
    val backgroundColor: Color,
    val emoji: String
)

internal fun generateEmojiData(name: String, colors: List<Color>): EmojiData {
    val numFromName = AvatarUtils.hashCode(name)
    val bgColor = AvatarUtils.getRandomColor(numFromName, colors, colors.size)
    val emojiIndex = AvatarUtils.getUnit(numFromName, EMOJI_POOL.size)

    return EmojiData(
        backgroundColor = bgColor,
        emoji = EMOJI_POOL[emojiIndex]
    )
}

@Composable
fun AvatarEmoji(
    name: String,
    colors: List<Color>,
    size: Dp = 40.dp,
    shape: Shape = CircleShape,
    modifier: Modifier = Modifier
) {
    val data = remember(name, colors) { generateEmojiData(name, colors) }

    Box(
        modifier = modifier
            .size(size)
            .clip(shape)
            .background(data.backgroundColor),
        contentAlignment = Alignment.Center
    ) {
        val fontSize = with(LocalDensity.current) { (size * 0.65f).toSp() }
        Text(
            text = data.emoji,
            fontSize = fontSize
        )
    }
}

